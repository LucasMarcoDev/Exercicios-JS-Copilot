const diaSemana = 'segunda-feira';

switch (diaSemana){
    case 'segunda-feira':
        console.log('dia de trabalho');
        break;
    case 'terça-feira':
        console.log('Dia de Muay Thai');
        break;
    case 'quarta-feira':
        console.log('Dia de torcer pelo S.P.F.C');
        break;
    case 'quinta-feira':
        console.log('Segundo dia de Muay Thai na semana');
        break;
    case 'sexta-feira':
        console.log('Dia de sair para passear');
        break;
    case 'Sábado':
        console.log('Dia de descanso e lazer');
        break;
        default:
        console.log('Dia de descanso para inicio de uma nova semana')
}



//Crie uma variável diaSemana com um dia da semana (ex: "segunda"). Use switch para mostrar o que você faz em cada dia.